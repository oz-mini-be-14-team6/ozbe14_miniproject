async def run():
    await Tortoise.init(config=TORTOISE_ORM)
    await Tortoise.generate_schemas()

    for q in questions:
        exists = await Question.filter(text=q).exists()
        if not exists:
            await Question.create(text=q)

    await Tortoise.close_connections()

if __name__ == "__main__":
    asyncio.run(run())
